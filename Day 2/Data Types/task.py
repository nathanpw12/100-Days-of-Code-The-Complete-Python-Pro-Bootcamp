###### 1- Strings = "Hello" ######

# Subscripting []

print("Hello"[4]) # Exibe o 4° caractere do a partir do inicio p/ fim da string
print("Hello"[-2]) # Exibe o 2° caractere do a partir do fim p/ o inicio da string

# Números em strings:

print("2" + "2") # é igual a "22"

###### 2- Integer = Numero Inteiro ######

print(2 + 2) # é igual a 4
print(10_000_000) # podemos usar underline como virgulas para separar as casas

###### 3- Float = Decimal ######

print(3.14) # usa-se . para representar os decimais

###### 4- Boolean ######
# Sempre começa com letra maiúscula
print(True)
print(False)

