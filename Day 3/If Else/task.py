############ if else ###############

print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

if height >= 120:
    print("You can ride the rollercoaster")
else:
    print("Sorry you have to grow taller before you can ride.")

#### Em português e usando metros #######

print("Bem vindo a Montanha Russa!")
altura = float(input("Qual sua altura em metros? exemplos: 1.60 | 1.80 | 1.20 ... "))
if altura >= 1.20:
    print("Párabens!, você pode entrar na Montanha Russa")
else:
    print("Infelizmente você não tem a altura mínima de 1.20 metros para entrar na Montanha Russa")

